# Variables: objects containing specific values

x = 5   #numeric or integer variable
print(x)

y = "We are learning python with Ammar" # string variable
print(y)

x = x+10  #or x=15 , variable value updated
print(x)

#types/class of variables
type(x) # no output will show for tihs
print(type(x))
print(type(y))

#Rules to assign a variable:
# 1- The variable should contain letters, numbers or underscores
# 2- Do not start with numbers
# 3- Spaces are not allowed
# 4- Do not use keywords used in functions (break, mean, media, test etc.)
# 5- Short and descriptive
# 6- Case sensitivity (Lowercase, uppercase letters, lowers case letter recommended)

fruit_basket= "Mangoes" #or "Mangoes , Apples" or 8
print(fruit_basket)
del fruit_basket  #deletes the variable
print(type(fruit_basket))
